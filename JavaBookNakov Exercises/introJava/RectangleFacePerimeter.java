package introJava;

public class RectangleFacePerimeter {

	public static void main(String[] args) {
		
		double a = 7.5;
		double h = 4.5;
		double face = a * h;
		double perimeter = 2 * (a + h);
		System.out.println("length = " + a + "\nheight = " + h
				+ "\nface = " + face + "\nperimeter = " + perimeter);
	}

}
